/**
 * Reads bottom-tabs lab sources from the example app and writes a typed module
 * for the docs site (click-to-view + copy in BottomTabsLabSourceExplorer).
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DOCS_ROOT = path.join(__dirname, '..');
const REPO_ROOT = path.join(DOCS_ROOT, '..');
const LAB = path.join(
  REPO_ROOT,
  'apps',
  'example',
  'src',
  'navigation',
  'bottomTabsLab',
);

const FILES = [
  ['shared/PlanckBridgedTabBar.tsx', path.join(LAB, 'shared', 'PlanckBridgedTabBar.tsx')],
  ['presets/presetKeys.ts', path.join(LAB, 'presets', 'presetKeys.ts')],
  ['presets/registry.ts', path.join(LAB, 'presets', 'registry.ts')],
  [
    'presets/plankBarV1/PlankBarV1TabNavigator.tsx',
    path.join(LAB, 'presets', 'plankBarV1', 'PlankBarV1TabNavigator.tsx'),
  ],
  ['presets/plankBarV1/plankBarV1.types.ts', path.join(LAB, 'presets', 'plankBarV1', 'plankBarV1.types.ts')],
  ['presets/plankBarV1/plankBarV1.icons.tsx', path.join(LAB, 'presets', 'plankBarV1', 'plankBarV1.icons.tsx')],
  ['presets/plankBarV1/plankBarV1.styles.ts', path.join(LAB, 'presets', 'plankBarV1', 'plankBarV1.styles.ts')],
  ['BottomTabsLabNavigator.tsx', path.join(LAB, 'BottomTabsLabNavigator.tsx')],
  ['types.ts', path.join(LAB, 'types.ts')],
  ['bottomTabsLabDrawerChrome.tsx', path.join(LAB, 'bottomTabsLabDrawerChrome.tsx')],
];

const outDir = path.join(DOCS_ROOT, 'data');
fs.mkdirSync(outDir, { recursive: true });

const entries = [];
for (const [id, abs] of FILES) {
  if (!fs.existsSync(abs)) {
    console.error('Missing source file:', abs);
    process.exit(1);
  }
  const content = fs.readFileSync(abs, 'utf8');
  entries.push(`  ${JSON.stringify(id)}: ${JSON.stringify(content)}`);
}

const ts = `/* eslint-disable */
/**
 * AUTO-GENERATED by docs/scripts/sync-bottom-tabs-lab-sources.mjs
 * Do not edit by hand — run: pnpm --filter @planckui/docs generate:sources
 */
export const BOTTOM_TABS_LAB_SOURCES = {
${entries.join(',\n')}
} as const;

export type BottomTabsLabSourceId = keyof typeof BOTTOM_TABS_LAB_SOURCES;
`;

const outPath = path.join(outDir, 'bottomTabsLabSources.generated.ts');
fs.writeFileSync(outPath, ts, 'utf8');
console.log('Wrote', outPath);
