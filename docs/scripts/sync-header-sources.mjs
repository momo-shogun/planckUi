/**
 * Generates docs/data/headerSources.generated.ts from repo source files
 * so the docs Header SourceExplorer stays aligned with packages/core.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '../..');
const OUT = path.join(__dirname, '../data/headerSources.generated.ts');

/** @type {{ id: string; file: string }[]} */
const FILES = [
  { id: 'Header/Header.tsx', file: 'packages/core/src/components/navigation/Header/Header.tsx' },
  { id: 'Header/Header.styles.ts', file: 'packages/core/src/components/navigation/Header/Header.styles.ts' },
  { id: 'Header/Header.types.ts', file: 'packages/core/src/components/navigation/Header/Header.types.ts' },
  { id: 'Header/index.ts', file: 'packages/core/src/components/navigation/Header/index.ts' },

  {
    id: 'PlanckH1V1/PlanckH1V1.tsx',
    file: 'packages/core/src/components/navigation/Header/PlanckH1V1/PlanckH1V1.tsx',
  },
  {
    id: 'PlanckH1V1/PlanckH1V1.styles.ts',
    file: 'packages/core/src/components/navigation/Header/PlanckH1V1/PlanckH1V1.styles.ts',
  },
  {
    id: 'PlanckH1V1/PlanckH1V1.types.ts',
    file: 'packages/core/src/components/navigation/Header/PlanckH1V1/PlanckH1V1.types.ts',
  },
  {
    id: 'PlanckH1V1/index.ts',
    file: 'packages/core/src/components/navigation/Header/PlanckH1V1/index.ts',
  },

  {
    id: 'ZeptoHeaderV1/ZeptoHeaderV1.tsx',
    file: 'packages/core/src/components/navigation/Header/ZeptoHeaderV1/ZeptoHeaderV1.tsx',
  },
  {
    id: 'ZeptoHeaderV1/ZeptoHeaderV1.styles.ts',
    file: 'packages/core/src/components/navigation/Header/ZeptoHeaderV1/ZeptoHeaderV1.styles.ts',
  },
  {
    id: 'ZeptoHeaderV1/ZeptoHeaderV1.types.ts',
    file: 'packages/core/src/components/navigation/Header/ZeptoHeaderV1/ZeptoHeaderV1.types.ts',
  },
  {
    id: 'ZeptoHeaderV1/ZeptoHeaderV1.icons.tsx',
    file: 'packages/core/src/components/navigation/Header/ZeptoHeaderV1/ZeptoHeaderV1.icons.tsx',
  },
  {
    id: 'ZeptoHeaderV1/index.ts',
    file: 'packages/core/src/components/navigation/Header/ZeptoHeaderV1/index.ts',
  },
];

function main() {
  /** @type {Record<string, string>} */
  const sources = {};
  for (const { id, file } of FILES) {
    const abs = path.join(REPO_ROOT, file);
    if (!fs.existsSync(abs)) {
      console.error(`sync-header-sources: missing file ${file}`);
      process.exitCode = 1;
      return;
    }
    sources[id] = fs.readFileSync(abs, 'utf8');
  }

  const body = Object.entries(sources)
    .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
    .join('\n');

  const header = `/* eslint-disable */
/**
 * AUTO-GENERATED by docs/scripts/sync-header-sources.mjs
 * Do not edit by hand — run: pnpm --filter @planckui/docs generate:sources
 */
`;

  const out = `${header}export const HEADER_SOURCES = {\n${body}\n} as const;\n\nexport type HeaderSourceId = keyof typeof HEADER_SOURCES;\n`;
  fs.writeFileSync(OUT, out, 'utf8');
  console.log(`wrote ${path.relative(REPO_ROOT, OUT)} (${Object.keys(sources).length} files)`);
}

main();
